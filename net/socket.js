exports.recalculateIV = function (pCurrentIV) {
	var newIV = new Uint8Array([0xF2, 0x53, 0x50, 0xC6]);
	
	for (var i = 0; i < 4; i++) {
		var input = pCurrentIV[i];
		var tableInput = ivShiftingKey[input];
		newIV[0] += (ivShiftingKey[newIV[1]] - input);
		newIV[1] -= (newIV[2] ^ tableInput);
		newIV[2] ^= (ivShiftingKey[newIV[3]] + input);
		newIV[3] -= (newIV[0] - tableInput);

		var val = (newIV[0] | (newIV[1] & 0xFF) << 8 | (newIV[2] & 0xFF) << 16 | (newIV[3] & 0xFF) << 24) >>> 0;
		var val2 = val >>> 0x1D;
		val = (val << 0x03) >>> 0;
		val2 |= val;
		newIV[0] = (val2 & 0xFF);
		newIV[1] = ((val2 >> 8) & 0xFF);
		newIV[2] = ((val2 >> 16) & 0xFF);
		newIV[3] = ((val2 >> 24) & 0xFF);
	}
	
	return newIV;
};

function RollLeft(value, shift) {
	var overflow = ((value >>> 0) << (shift % 8)) >>> 0;
	var ret = ((overflow & 0xFF) | (overflow >>> 8)) & 0xFF;
	return ret;
}

function RollRight(value, shift) {
	var overflow = (((value >>> 0) << 8) >>> (shift % 8));
	var ret = ((overflow & 0xFF) | (overflow >>> 8)) & 0xFF;
	return ret;
}

exports.encryptMapleCrypto = function (pData) {	
	var length = pData.length, j;
	var a, c;
	for (var i = 0; i < 3; i++)
	{
		a = 0;
		for (j = length; j > 0; j--)
		{
			c = pData[length - j];
			c = RollLeft(c, 3);
			c += j;
			c &= 0xFF; // Addition
			c ^= a;
			a = c;
			c = RollRight(a, j);
			c ^= 0xFF;
			c += 0x48;
			c &= 0xFF; // Addition
			pData[length - j] = c;
		}
		a = 0;
		for (j = length; j > 0; j--)
		{
			c = pData[j - 1];
			c = RollLeft(c, 4);
			c += j;
			c &= 0xFF; // Addition
			c ^= a;
			a = c;
			c ^= 0x13;
			c = RollRight(c, 3);
			pData[j - 1] = c;
		}
	}
};

exports.decryptMapleCrypto = function (pData) {
	var length = pData.length, j;
	var a, b, c;
	for (var i = 0; i < 3; i++)
	{
		a = 0;
		b = 0;
		for (j = length; j > 0; j--)
		{
			c = pData[j - 1];
			c = RollLeft(c, 3);
			c ^= 0x13;
			a = c;
			c ^= b;
			c -= j;
			c &= 0xFF; // Addition
			c = RollRight(c, 4);
			b = a;
			pData[j - 1] = c;
		}
		a = 0;
		b = 0;
		for (j = length; j > 0; j--)
		{
			c = pData[length - j];
			c -= 0x48;
			c &= 0xFF; // Addition
			c ^= 0xFF;
			c = RollLeft(c, j);
			a = c;
			c ^= b;
			c -= j;
			c &= 0xFF; // Addition
			c = RollRight(c, 3);
			b = a;
			pData[length - j] = c;
		}
	}
};


