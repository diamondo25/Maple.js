exports.getLengthFromHeader = function (pData) {
	var length = pData[0] | pData[1] << 8 | pData[2] << 16 | pData[3] << 24;
	return ((length >>> 16) ^ (length & 0xFFFF)) & 0xFFFF;
};


exports.generateHeader = function (pData, pIV, pLength, pVersion) {
	var a = pIV[2] | pIV[3] << 8;
	a ^= pVersion;
	var b = a ^ pLength;
	
	pData[0] = a & 0xFF;
	pData[1] = (a >>> 8) & 0xFF;
	pData[2] = b & 0xFF;
	pData[3] = (b >>> 8) & 0xFF;
};

